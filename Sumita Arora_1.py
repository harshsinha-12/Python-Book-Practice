# Data Types

print(bool(1))
print(bool(0))


# Complex number
a = 3 + 5j
print(a)

# Real part
print(a.real)

# Imaginary part
print(a.imag)

# Conjugate
print(a.conjugate())

# Absolute value
print(abs(a))

